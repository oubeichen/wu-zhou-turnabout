function 建音频() {
  if (音频) return 音频;
  const 语境 = new (window.AudioContext || window.webkitAudioContext)();
  const 主 = 语境.createGain();
  主.gain.value = 0.18;
  主.connect(语境.destination);
  音频 = { 语境, 主, 开: false };
  return 音频;
}

function 弹音(频率, 延迟, 时长, 类型 = "sine") {
  const 声 = 建音频();
  const 起 = 声.语境.currentTime + 延迟;
  const 振 = 声.语境.createOscillator();
  const 量 = 声.语境.createGain();
  振.type = 类型;
  振.frequency.setValueAtTime(频率, 起);
  量.gain.setValueAtTime(0.0001, 起);
  量.gain.exponentialRampToValueAtTime(0.28, 起 + 0.04);
  量.gain.exponentialRampToValueAtTime(0.0001, 起 + 时长);
  振.connect(量);
  量.connect(声.主);
  振.start(起);
  振.stop(起 + 时长 + 0.04);
}

function 一段宫乐() {
  const 音 = [196, 220, 247, 294, 330, 392];
  const 根音 = 音[Math.floor(Math.random() * 音.length)];
  弹音(根音, 0, 1.4, "triangle");
  弹音(根音 * 1.5, 0.42, 1.1, "sine");
  弹音(根音 * 2, 1.05, 0.7, "sine");
}

