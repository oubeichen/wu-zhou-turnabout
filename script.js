function 完成打字() {
  window.clearInterval(字计时);
  文本框.textContent = 完整文本;
  正在打字 = false;
  const 幕 = 找幕(当前);
  if (!幕) return;
  if (当前段 < 取段落(幕).length - 1) {
    渲染继续(幕);
  } else {
    提示.textContent = "";
    渲染选项(幕);
  }
}

