function 打字(字) {
  window.clearInterval(字计时);
  正在打字 = true;
  完整文本 = 字;
  文本框.textContent = "";
  选择区.innerHTML = "";
  let 位 = 0;
  const 速度 = 自动 ? 10 : 22;
  字计时 = window.setInterval(() => {
    位 += 1;
    文本框.textContent = 完整文本.slice(0, 位);
    if (位 >= 完整文本.length) {
      完成打字();
    }
  }, 速度);
}

function 完成打字() {
  window.clearInterval(字计时);
  文本框.textContent = 完整文本;
  正在打字 = false;
  const 幕 = 当前 === "end" ? 生成结局() : 找幕(当前);
  if (当前 === "end") {
    选择区.innerHTML = "";
    const 重开 = document.createElement("button");
    重开.type = "button";
    重开.textContent = "从第一幕重新抉择";
    重开.addEventListener("click", 重开游戏);
    选择区.append(重开);
  } else {
    渲染选项(幕);
  }
}

function 立即显示() {
  if (!正在打字) return;
  完成打字();
}

